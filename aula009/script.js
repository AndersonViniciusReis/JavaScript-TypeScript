//ecmascript 2015 (ES6) - let e const
//let - variável de escopo de bloco, ou seja, só pode ser acessada dentro do bloco onde foi declarada
//const - variável de escopo de bloco, ou seja, só pode ser acessada dentro do bloco onde foi declarada, e não pode ser reatribuída
var nome = 'Luiz';
var nome = 'Otávio';
console.log(nome); // Otávio
//não podemos redeclarar uma variável com let ou const, ou seja, não podemos declarar a mesma variável mais de uma vez
