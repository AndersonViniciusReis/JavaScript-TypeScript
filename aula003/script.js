//Strings podem ser delimitadas por aspas simples, duplas ou crases (template literals)
// strings são iguais texto, palavras, frases, etc. Tudo que estiver entre aspas é considerado uma string
console.log("Luiz 'Otávio'"); // Aspas duplas permitem aspas simples sem necessidade de escape
console.log('Luiz "Otávio"'); // Aspas simples permitem aspas duplas sem necessidade de escape
console.log(`'Luiz' "Otávio"`); // Template literals permitem ambas as formas de aspas
//number são iguais a números, ou seja, 15 é igual a 15.00, por exemplo. O JavaScript não diferencia inteiros de decimais 
//virgula é usada para separar os argumentos dentro do console.log, ou seja, cada argumento é separado por vírgula
//ponto é usado para acessar propriedades e métodos de objetos, ou seja, quando queremos acessar uma propriedade ou método de um objeto, usamos o ponto para separar o nome do objeto do nome da propriedade ou método

console.log(35, 15.85, 'Luiz Otávio de novo'); 

